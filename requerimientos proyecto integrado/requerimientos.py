# 1. Requisitos Funcionales

#     a. Home Page
#         •	Mostrar una galería destacada de obras de arte.
#         •	Mostrar un menú de navegación con las secciones: "Artistas", "Técnicas Artísticas", "Contacto", y "Reservas".
#             Versión 2
#             •	Ofrecer un buscador para filtrar obras por artista, técnica o nombre de la obra.
#             •	Mostrar eventos o exposiciones virtuales destacadas.
#     b. Sección por Artistas
#         •	Lista de artistas con su perfil, biografía y obras disponibles.
#         •	Ficha de artista con:
#             o	Imagen del artista.
#             o	Breve descripción, biografía y/o visión del artista.
#             o	Lista de obras con imágenes, título, técnica y precio (si es aplicable).
#             o	Botón para reservar la obra 
#     c. Sección por Técnicas Artísticas
#         •	Mostrar un listado de técnicas artísticas (óleo, acuarela, escultura, etc.).
#         •	Filtrar obras según la técnica seleccionada.
#         •	Mostrar detalles de las obras filtradas: título, autor, técnica, precio y estado de mercado.
#     d. Reserva de Obras
#         •	Proceso de reserva de obras, donde el usuario pueda:
#             o	Seleccionar la obra.
#             o	Introducir sus datos de contacto.
#             o	Confirmar la reserva.
#             	Inicialmente se hará efectiva después de 24 horas para evitar reservas duplicadas
#             o	Recibir una notificación de confirmación por email.
#     e. Canal de Comunicación Artista-Galería
#         •	Formulario de contacto para cada artista, donde los interesados puedan enviar consultas sobre las obras o el artista.
#         •	Sistema de notificación por email para el artista y la galería cuando se recibe una consulta.
#             Version2(Posiblemente no alcanzable para el semestre académico)
#     1. 	Gestión de Usuarios
#         •	Opción de registro para usuarios recurrentes.
#         •	Perfil de usuario con historial de reservas y comunicaciones realizadas.
#         •	Control de acceso para administradores de la galería para gestionar artistas y obras.
# 2. Requisitos No Funcionales

#     a. Usabilidad
#         •	La interfaz debe ser intuitiva, simple de navegar y estéticamente atractiva para reflejar la calidad de las obras de arte.
#     b. Escalabilidad
#         •	El sistema debe permitir la adición de más artistas y obras sin perder rendimiento.
#     e. Compatibilidad
#         •	Debe ser compatible con dispositivos móviles y ordenadores (diseño responsive).
#     f. SEO y accesibilidad
#         •	Optimización para motores de búsqueda (SEO) para que las obras y los artistas sean fácilmente localizables.
#         •	Cumplir con estándares de accesibilidad para personas con discapacidades.
# 3. Flujo General del Usuario
#         •	Un usuario ingresa a la Home Page y puede explorar las obras destacadas o navegar por artistas o técnicas.
#         •	Si le interesa una obra, puede ver los detalles en la ficha del artista o técnica, y optar por reservar la obra o contactar con la galería o artista.
#         •	Si decide reservar, sigue un proceso simple de contacto y confirmación.


#         Version 2 (Posiblemente no alcanzable para el semestre académico)
#     c. Rendimiento
#         •	Las imágenes de las obras deben cargarse rápidamente, utilizando técnicas de optimización de imágenes.
#     d. Seguridad
#         •	Protección de los datos personales de los usuarios (reservas y comunicación).
#         •	Certificado SSL para asegurar la transmisión de datos.
